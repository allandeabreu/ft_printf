#include "ft_printf.c"

int ft_put_s(char *s)
{
    int i;

    i = 0;
    if (!s)
        s = "(null)";
    while (s[i] != '\0')
    {
        write(1, &s[i], 1);
        i++;
    }
    return (i);
}
